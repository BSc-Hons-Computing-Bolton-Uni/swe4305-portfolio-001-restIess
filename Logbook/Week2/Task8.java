package Logbook.Week2;

public class Task8 {
    public static void main(String[] args) {
        // prints even numbers from 1 to 20
        System.out.println("Even numbers from 1 to 20:");
        for (int i = 1; i <= 10; i++) {
            int evenNumber = i * 2;
            System.out.print(evenNumber + " ");
        }
        System.out.println();
    }
}
