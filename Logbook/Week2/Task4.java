package Logbook.Week2;

public class Task4 {
    public static void main(String[] args) {
        // Prints the seven times multiplication table
        System.out.println("Seven times table:");
        for (int i = 1; i <= 12; i++) {
            System.out.println(i + " x 7 = " + (i * 7));
        }
    }
}
